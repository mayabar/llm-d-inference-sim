// v1/chat/completions
// Message defines vLLM chat completions Message
type Message struct {
	// Role is the message Role, optional values are 'user', 'assistant', ...
	Role string `json:"role,omitempty"`
	// Content defines text of this message
	Content ChatComplContent `json:"content,omitempty"`
	// ToolCalls are the tool calls created by the model
	ToolCalls []ToolCall `json:"tool_calls,omitempty"`
}

func (m *Message) PlainText(includeRole bool) string {
	var builder strings.Builder

	if includeRole {
		builder.WriteString(m.Role)
		builder.WriteString(": ")
	}

	if m.Content.Raw != "" {
		builder.WriteString(m.Content.Raw)
	} else {
		var parts []string
		for _, block := range m.Content.Structured {
			switch block.Type {
			case "text":
				parts = append(parts, block.Text)
			case "image_url":
				parts = append(parts, "image: "+block.ImageURL.Url)
			}
		}

		builder.WriteString(strings.Join(parts, "\n"))
	}

	return builder.String()
}

type ChatComplContent struct {
	Raw        string
	Structured []ChatComplContentBlock
}

type ChatComplContentBlock struct {
	Type     string              `json:"type"`
	Text     string              `json:"text,omitempty"`
	ImageURL ChatComplImageBlock `json:"image_url,omitempty"`
}

type ChatComplImageBlock struct {
	Url string `json:"url,omitempty"`
}

// UnmarshalJSON allow use both format
func (mc *ChatComplContent) UnmarshalJSON(data []byte) error {
	// Raw format
	var str string
	if err := json.Unmarshal(data, &str); err == nil {
		mc.Raw = str
		return nil
	}

	// Block format
	var blocks []ChatComplContentBlock
	if err := json.Unmarshal(data, &blocks); err == nil {
		mc.Structured = blocks
		return nil
	}

	return errors.New("content format not supported")
}

func (mc ChatComplContent) MarshalJSON() ([]byte, error) {
	if mc.Raw != "" {
		return json.Marshal(mc.Raw)
	}
	if mc.Structured != nil {
		return json.Marshal(mc.Structured)
	}
	return json.Marshal("")
}

// FunctionCall defines a tool call generated by the model including its arguments
type FunctionCall struct {
	// Name is the function's name, can be null in streaming in not the first chunk
	Name *string `json:"name"`
	// Arguments are the arguments of the function call
	Arguments string `json:"arguments,omitempty"`
	// TokenizedArguments contains tokenized arguments
	tokenizedArguments *Tokenized
}

func (fc *FunctionCall) SetTokenizedArguments(tokenized *Tokenized) {
	fc.tokenizedArguments = tokenized
}

func (fc *FunctionCall) TokenizedArguments() *Tokenized {
	return fc.tokenizedArguments
}

// ToolCall defines a tool call generated by the model
type ToolCall struct {
	// Function is a tool call generated by the model
	Function FunctionCall `json:"function"`
	// ID is the ID of the tool call
	ID string `json:"id"`
	// Type is the type of the tool, only functions are supported
	Type string `json:"type"`
	// Index is the index of the tool in the sequence of tools generated by the model
	Index int `json:"index"`
}

