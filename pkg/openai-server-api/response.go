// Response interface representing response types
type Response interface {
	GetRequestID() string
}

// baseResponse contains base response related information
type baseResponse struct {
	// ID defines the response ID
	ID string `json:"id,omitempty"`
	// Model defines the Model name for current request
	Model string `json:"model,omitempty"`
	// Object is the Object type, "text_completion", "chat.completion", or "chat.completion.chunk"
	Object string `json:"object,omitempty"`
	// KVParams kv transfer related fields
	KVParams *KVTransferParams `json:"kv_transfer_params"`
	// RequestID is the unique request ID for tracking
	RequestID string `json:"-"`
}

// baseCompletionsResponse contains base completions response related information
type baseCompletionsResponse struct {
	baseResponse
	// Created defines the response creation timestamp
	Created int64 `json:"created"`
	// Usage contains the token usage statistics for the request
	Usage *Usage `json:"usage"`
}

// Usage contains token Usage statistics
type Usage struct {
	// PromptTokens is the number of tokens in the prompt
	PromptTokens int `json:"prompt_tokens"`
	// CompletionTokens is the number of tokens generated by the model as the response
	CompletionTokens int `json:"completion_tokens"`
	// TotalTokens is the total number of tokens processed for the request (the sum of the two values above)
	TotalTokens int `json:"total_tokens"`
	// PromptTokensDetail contains detailed token usage statistics for the prompt
	PromptTokensDetail *PromptTokensDetail `json:"prompt_tokens_detail"`
}

// PromptTokensDetail contains detailed token usage statistics for the prompt
type PromptTokensDetail struct {
	// CacheTokens is the number of tokens in the prompt that are in the local KV Cache
	CachedTokens int `json:"cached_tokens"`
}

// LogprobsContent represents logprobs for a single token in chat completions
type LogprobsContent struct {
	// Token is the token string
	Token string `json:"token"`
	// Logprob is the log probability of the token
	Logprob float64 `json:"logprob"`
	// Bytes is the byte representation of the token
	Bytes []int `json:"bytes"`
	// TopLogprobs is the list of top alternative tokens along their log probabilities
	TopLogprobs []LogprobsContent `json:"top_logprobs,omitempty"`
}

// ChatLogprobs represents logprobs for chat completion responses
type ChatLogprobs struct {
	// Content is an array of logprobs for each token in the content
	Content []LogprobsContent `json:"content"`
}

// TextLogprobs represents logprobs for text completion responses
type TextLogprobs struct {
	// Tokens is an array of tokens
	Tokens []string `json:"tokens"`
	// TokenLogprobs is an array of log probabilities for each token
	TokenLogprobs []float64 `json:"token_logprobs"`
	// TopLogprobs is an array of objects containing the top alternative tokens
	TopLogprobs []map[string]float64 `json:"top_logprobs"`
	// TextOffset is an array of character offsets
	TextOffset []int `json:"text_offset"`
}

